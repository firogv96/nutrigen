window.I18nHelper = {
  get: function (lang, key) {
    if (!window.I18N[lang] || window.I18N[lang][key] === undefined) {
      // Fallback to ES if EN missing
      if (window.I18N["es"] && window.I18N["es"][key] !== undefined) {
        return window.I18N["es"][key];
      }
      return key; // return key as fallback
    }
    return window.I18N[lang][key];
  },
  updateUI: function (lang) {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      let key = el.getAttribute("data-i18n");
      el.innerHTML = this.get(lang, key);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      let key = el.getAttribute("data-i18n-placeholder");
      el.setAttribute("placeholder", this.get(lang, key));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      let key = el.getAttribute("data-i18n-title");
      el.setAttribute("title", this.get(lang, key));
    });
  },
};
